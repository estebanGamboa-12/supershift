"use client"

import Image from "next/image"
import { useMemo, useState } from "react"
import { motion } from "framer-motion"
import type { UserSummary } from "@/types/users"
import { getSupabaseBrowserClient } from "@/lib/supabase"

type UserAuthPanelProps = {
  users: UserSummary[]
  onLogin: (user: UserSummary) => void
  onUserCreated: (user: UserSummary) => void
}

function GoogleIcon({ className }: { className?: string }) {
  return (
    <svg className={className} viewBox="0 0 24 24" aria-hidden>
      <path
        fill="currentColor"
        d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
      />
      <path
        fill="currentColor"
        d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
      />
      <path
        fill="currentColor"
        d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
      />
      <path
        fill="currentColor"
        d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
      />
    </svg>
  )
}

function Spinner() {
  return (
    <motion.div
      className="h-5 w-5 rounded-full border-2 border-white/40 border-t-white"
      animate={{ rotate: 360 }}
      transition={{ repeat: Infinity, duration: 1, ease: "linear" }}
    />
  )
}

export default function UserAuthPanel({
  users,
  onLogin,
  onUserCreated: _onUserCreated,
}: UserAuthPanelProps) {
  const [loginError, setLoginError] = useState("")
  const [isLoggingIn, setIsLoggingIn] = useState(false)

  const supabase = useMemo(() => {
    if (typeof window === "undefined") {
      return null
    }

    try {
      return getSupabaseBrowserClient()
    } catch (error) {
      console.error("Supabase client could not be initialised", error)
      return null
    }
  }, [])

  const handleGoogleLogin = async () => {
    setLoginError("")
    if (!supabase) {
      setLoginError(
        "Supabase no está configurado correctamente. Revisa las variables de entorno.",
      )
      return
    }

    try {
      setIsLoggingIn(true)
      const origin = typeof window !== "undefined" ? window.location.origin : ""
      const redirectTo = origin ? `${origin}/auth/callback` : undefined
      const { error } = await supabase.auth.signInWithOAuth({
        provider: "google",
        options: {
          redirectTo,
        },
      })

      if (error) {
        throw error
      }
    } catch (error) {
      if (error instanceof Error) {
        const normalisedMessage = error.message.toLowerCase()
        if (
          normalisedMessage.includes("provider is not enabled") ||
          normalisedMessage.includes("unsupported provider")
        ) {
          setLoginError(
            "El inicio de sesión con Google no está habilitado. Activa el proveedor de Google en el panel de Supabase (Authentication → Providers) y configura las credenciales correspondientes.",
          )
          return
        }

        setLoginError(error.message)
        return
      }

      setLoginError("No se pudo iniciar sesión con Google")
    } finally {
      setIsLoggingIn(false)
    }
  }

  return (
    <div className="relative mx-auto w-full max-w-md overflow-hidden rounded-3xl border border-white/10 bg-slate-900/60 p-10 text-white shadow-2xl backdrop-blur-xl">
      <div className="pointer-events-none absolute inset-0 bg-gradient-to-b from-blue-500/10 via-purple-500/5 to-transparent opacity-80" />

      <div className="relative z-10 flex flex-col items-center gap-8 text-center">
        <div className="relative">
          <span
            className="pointer-events-none absolute inset-0 -translate-y-1 scale-125 rounded-full bg-cyan-400/25 blur-xl"
            aria-hidden
          />
          <div className="relative grid h-20 w-20 place-items-center overflow-hidden rounded-2xl border border-white/10 bg-slate-950/80 shadow-xl shadow-blue-500/20">
            <Image
              src="/planloop-logo.svg"
              alt="Planloop"
              width={64}
              height={64}
              priority
              className="h-16 w-16"
            />
          </div>
        </div>

        <div className="space-y-2">
          <h1 className="text-2xl font-semibold tracking-tight text-white">
            Bienvenido a tu turno
          </h1>
          <p className="text-sm text-white/60">
            Un clic y listo. Sin contraseñas.
          </p>
        </div>

        {loginError && (
          <p className="text-sm text-center text-red-400">
            {loginError}
          </p>
        )}

        <button
          type="button"
          onClick={handleGoogleLogin}
          disabled={isLoggingIn}
          className="flex items-center justify-center gap-3 w-full rounded-xl border border-white/15 bg-white/5 px-5 py-3.5 text-sm font-semibold text-white transition hover:border-white/30 hover:bg-white/10 disabled:opacity-60"
        >
          {isLoggingIn ? (
            <Spinner />
          ) : (
            <GoogleIcon className="h-5 w-5 text-white" />
          )}
          <span>{isLoggingIn ? "Redirigiendo…" : "Continuar con Google"}</span>
        </button>
      </div>

      <p className="relative z-10 mt-10 text-center text-[11px] tracking-[0.3em] text-white/40">
        <a
          href="https://www.esteban-dev.com/"
          target="_blank"
          rel="noopener noreferrer"
          className="hover:text-white/70 transition"
        >
          Diseñado por Esteban Gamboa
        </a>
      </p>
    </div>
  )
}
